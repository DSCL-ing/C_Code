//稳定排序
/*
（相同数的相对前后位置在排序后没有改变。）
* 解释：排在前面的数，在排完序后还是在前面。
* 作用举例：同分的同学中先考完试的排在前面


* 不同方法稳定性判断依据：代码能够控制实现相对位置不变，如插入排序可以稳定可以不稳定，
只要能稳定就是稳定排序


* 稳定排序有：
插入排序：插入可控
冒泡排序：交换可控
归并排序：归并可控

* 不稳定排序有：
希尔排序：预排序
堆排序：建堆方法不同得到的堆不一样，一个子树中，相同大小的数的位置不一定一样
选择排序：选择、排序后先选到的排最外，相对逆序
快速排序：找大找小交换

*/


