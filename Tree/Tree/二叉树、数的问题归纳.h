/*
遍历命名

N(Node）、L(Left subtree）和R(Right subtree）

根据访问结点操作发生位置命名：
① NLR：前序遍历(Preorder Traversal 亦称（先序遍历））
――访问根结点的操作发生在遍历其左右子树之前。
② LNR：中序遍历(Inorder Traversal)
――访问根结点的操作发生在遍历其左右子树之中（间）。
③ LRN：后序遍历(Postorder Traversal)
――访问根结点的操作发生在遍历其左右子树之后。

前三种次序与后三种次序对称

*/